#include <stdio.h>
#include <stdlib.h>
/*---------------------------------------------------------------------------------------*/
#define MAX 100
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
void cargaVector (int *vec, int d);
void muestraVector (int *vec, int d);
int crearVectorsinRepetidos (int *vec, int d);
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
int main (void){
    int vector[MAX];
    int dim;
    int i;
    int repetidosVec;
    int repetidosMain = 0;

    
    do{
        printf("Introduce la dimension del vector (entre 1 y %d): ", MAX);
        scanf("%d", &dim);
    }
        while (dim<=0||dim>MAX);
    
    cargaVector(vector,dim);
    printf("\n\n");
    printf("||---------------------------------------------------------------||\n");
    printf("\tEl vector introducido es:\n");
    muestraVector(vector,dim);
    printf("||---------------------------------------------------------------||");
    repetidosVec = crearVectorsinRepetidos (vector,dim);
    
    for (i=0;i<dim;i++)
        if (vector[i]==-1)
           repetidosMain++;
    printf("\n\n\n\n");
    
    printf("||---------------------------------------------------------------||\n");
    if (repetidosMain != repetidosVec)
       printf("ERROR.");
    else{
        printf("\tEl numero de elementos repetidos es %d", repetidosVec);
        printf("\n");
        printf("||---------------------------------------------------------------||");
        printf("\n\n\n\n");
        
        printf("||---------------------------------------------------------------||\n");
        printf("\tEl vector resultante es:\n");
        muestraVector(vector,dim);
    }
    printf("||---------------------------------------------------------------||");
    
    printf("\n\n\n\n");
    system ("pause");
    return 0;
}
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
void cargaVector (int *vec, int d){
    int i;
    
    for (i=0;i<d;i++){
        do{
           printf("Introduce el elemento [%d] del vector (ha de ser positivo): ", i+1);
           scanf("%d", &vec[i]);
        }
        while (vec[i]<0);
    }
}
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
void muestraVector (int *vec, int d){
     int i;
     
     for (i=0;i<d;i++)
         printf("\t%d", vec[i]);
     printf("\n");
}
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/
int crearVectorsinRepetidos (int *vec, int d){
    int i;
    int f;
    int comprobar;
    int repetidos = 0;
    
    for (i=0;i<d;i++){
       comprobar = vec[i];
       for (f=i+1;f<d;f++)
           if ((vec[f]!=-1) && (vec[f]==comprobar)){
              vec[f]=-1;
              repetidos++;
           }
    }
    
    return repetidos;
}
